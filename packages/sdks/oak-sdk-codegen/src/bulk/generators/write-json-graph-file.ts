/**
 * Prerequisite graph JSON dataset descriptor and writer.
 *
 * @remarks
 * Defines the prerequisite-graph-specific content for the three-file
 * JSON dataset pattern (`data.json`, `types.ts`, `index.ts`) and
 * delegates the mechanical write to {@link writeJsonDataset}.
 */
import type { PrerequisiteGraph } from './prerequisite-graph-generator.js';
import { writeJsonDataset, type JsonDatasetDescriptor } from './write-json-dataset.js';

const prerequisiteTypesModuleLines: readonly string[] = [
  '/**',
  ' * Type definitions for the prerequisite graph.',
  ' *',
  ' * @remarks',
  ' * Generated by the prerequisite graph JSON writer.',
  ' * These interfaces describe the shape of `data.json`.',
  ' */',
  '',
  '/**',
  ' * A prerequisite edge in the graph.',
  ' */',
  'export interface PrerequisiteEdge {',
  '  readonly from: string;',
  '  readonly to: string;',
  "  readonly rel: 'prerequisiteFor';",
  "  readonly source: 'thread' | 'priorKnowledge';",
  '}',
  '',
  '/**',
  ' * A unit node in the prerequisite graph.',
  ' */',
  'export interface PrerequisiteNode {',
  '  readonly unitSlug: string;',
  '  readonly unitTitle: string;',
  '  readonly subject: string;',
  '  readonly keyStage: string;',
  '  readonly year: number | undefined;',
  '  readonly priorKnowledge: readonly string[];',
  '  readonly threadSlugs: readonly string[];',
  '}',
  '',
  '/**',
  ' * Statistics about the prerequisite graph.',
  ' */',
  'export interface PrerequisiteGraphStats {',
  '  readonly unitsWithPrerequisites: number;',
  '  readonly totalEdges: number;',
  '  readonly subjectsCovered: readonly string[];',
  '}',
  '',
  '/**',
  ' * The complete prerequisite graph structure.',
  ' */',
  'export interface PrerequisiteGraph {',
  '  readonly version: string;',
  '  readonly generatedAt: string;',
  '  readonly sourceVersion: string;',
  '  readonly stats: PrerequisiteGraphStats;',
  '  readonly nodes: readonly PrerequisiteNode[];',
  '  readonly edges: readonly PrerequisiteEdge[];',
  '  readonly seeAlso: string;',
  '}',
  '',
];

const prerequisiteIndexModuleLines: readonly string[] = [
  '/**',
  ' * Prerequisite graph — typed loader for generated JSON data.',
  ' *',
  ' * @remarks',
  ' * Generated by the prerequisite graph JSON writer.',
  ' * Imports raw JSON data and re-exports it with TypeScript interfaces.',
  ' */',
  "import { createRequire } from 'node:module';",
  'import type {',
  '  PrerequisiteEdge,',
  '  PrerequisiteGraph,',
  '  PrerequisiteGraphStats,',
  '  PrerequisiteNode,',
  "} from './types.js';",
  '',
  'const require = createRequire(import.meta.url);',
  "const data: JsonPrerequisiteGraph = require('./data.json');",
  '',
  'interface JsonPrerequisiteEdge {',
  '  readonly from: string;',
  '  readonly to: string;',
  '  readonly rel: string;',
  '  readonly source: string;',
  '}',
  '',
  'interface JsonPrerequisiteNode {',
  '  readonly unitSlug: string;',
  '  readonly unitTitle: string;',
  '  readonly subject: string;',
  '  readonly keyStage: string;',
  '  readonly year?: number;',
  '  readonly priorKnowledge: readonly string[];',
  '  readonly threadSlugs: readonly string[];',
  '}',
  '',
  'interface JsonPrerequisiteGraph {',
  '  readonly version: string;',
  '  readonly generatedAt: string;',
  '  readonly sourceVersion: string;',
  '  readonly stats: PrerequisiteGraphStats;',
  '  readonly nodes: readonly JsonPrerequisiteNode[];',
  '  readonly edges: readonly JsonPrerequisiteEdge[];',
  '  readonly seeAlso: string;',
  '}',
  '',
  'function createPrerequisiteEdge(edge: JsonPrerequisiteEdge): PrerequisiteEdge {',
  "  if (edge.rel !== 'prerequisiteFor') {",
  '    throw new TypeError(`Unexpected prerequisite edge relation: ${edge.rel}`);',
  '  }',
  '',
  "  if (edge.source !== 'thread' && edge.source !== 'priorKnowledge') {",
  '    throw new TypeError(`Unexpected prerequisite edge source: ${edge.source}`);',
  '  }',
  '',
  '  return {',
  '    from: edge.from,',
  '    to: edge.to,',
  '    rel: edge.rel,',
  '    source: edge.source,',
  '  };',
  '}',
  '',
  'function createPrerequisiteNode(node: JsonPrerequisiteNode): PrerequisiteNode {',
  '  return {',
  '    unitSlug: node.unitSlug,',
  '    unitTitle: node.unitTitle,',
  '    subject: node.subject,',
  '    keyStage: node.keyStage,',
  '    year: node.year,',
  '    priorKnowledge: node.priorKnowledge,',
  '    threadSlugs: node.threadSlugs,',
  '  };',
  '}',
  '',
  'function createPrerequisiteGraph(graph: JsonPrerequisiteGraph): PrerequisiteGraph {',
  '  return {',
  '    version: graph.version,',
  '    generatedAt: graph.generatedAt,',
  '    sourceVersion: graph.sourceVersion,',
  '    stats: graph.stats,',
  '    nodes: graph.nodes.map(createPrerequisiteNode),',
  '    edges: graph.edges.map(createPrerequisiteEdge),',
  '    seeAlso: graph.seeAlso,',
  '  };',
  '}',
  '',
  '/**',
  ' * Prerequisite graph with unit dependencies.',
  ' *',
  ' * @remarks',
  ' * Contains unit prerequisites and learning dependencies.',
  ' * Use for "what comes before" queries and learning path planning.',
  ' */',
  'export const prerequisiteGraph: PrerequisiteGraph = createPrerequisiteGraph(data);',
  '',
  'export type {',
  '  PrerequisiteEdge,',
  '  PrerequisiteGraph,',
  '  PrerequisiteGraphStats,',
  '  PrerequisiteNode,',
  "} from './types.js';",
  '',
];

/**
 * Prerequisite graph dataset descriptor.
 *
 * @remarks
 * Provides the prerequisite-graph-specific types and loader content
 * for the generic JSON dataset writer.
 */
export const prerequisiteGraphDescriptor: JsonDatasetDescriptor = {
  directoryName: 'prerequisite-graph',
  typesModuleContent: prerequisiteTypesModuleLines.join('\n'),
  indexModuleContent: prerequisiteIndexModuleLines.join('\n'),
};

/**
 * Writes prerequisite graph output as JSON plus typed TypeScript modules.
 *
 * @param graph - Graph data to write
 * @param outputDir - Parent output directory
 * @returns Path to the created `prerequisite-graph` directory
 */
export async function writePrerequisiteGraphAsJson(
  graph: PrerequisiteGraph,
  outputDir: string,
): Promise<string> {
  return writeJsonDataset(prerequisiteGraphDescriptor, graph, outputDir);
}
