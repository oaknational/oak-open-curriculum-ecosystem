/**
 * Prior knowledge graph JSON dataset descriptor and writer.
 *
 * @remarks
 * Defines the prior-knowledge-graph-specific content for the three-file
 * JSON dataset pattern (`data.json`, `types.ts`, `index.ts`) and
 * delegates the mechanical write to {@link writeJsonDataset}.
 */
import type { PriorKnowledgeGraph } from './prior-knowledge-graph-generator.js';
import { writeJsonDataset, type JsonDatasetDescriptor } from './write-json-dataset.js';

const priorKnowledgeTypesModuleLines: readonly string[] = [
  '/**',
  ' * Type definitions for the prior knowledge graph.',
  ' *',
  ' * @remarks',
  ' * Generated by the prior knowledge graph JSON writer.',
  ' * These interfaces describe the shape of `data.json`.',
  ' */',
  '',
  '/**',
  ' * A prior knowledge edge in the graph.',
  ' */',
  'export interface PriorKnowledgeEdge {',
  '  readonly from: string;',
  '  readonly to: string;',
  "  readonly rel: 'prerequisiteFor';",
  "  readonly source: 'thread' | 'priorKnowledge';",
  '}',
  '',
  '/**',
  ' * A unit node in the prior knowledge graph.',
  ' */',
  'export interface PriorKnowledgeNode {',
  '  readonly unitSlug: string;',
  '  readonly unitTitle: string;',
  '  readonly subject: string;',
  '  readonly keyStage: string;',
  '  readonly year: number | undefined;',
  '  readonly priorKnowledge: readonly string[];',
  '  readonly threadSlugs: readonly string[];',
  '}',
  '',
  '/**',
  ' * Statistics about the prior knowledge graph.',
  ' */',
  'export interface PriorKnowledgeGraphStats {',
  '  readonly unitsWithPrerequisites: number;',
  '  readonly totalEdges: number;',
  '  readonly subjectsCovered: readonly string[];',
  '}',
  '',
  '/**',
  ' * The complete prior knowledge graph structure.',
  ' */',
  'export interface PriorKnowledgeGraph {',
  '  readonly version: string;',
  '  readonly generatedAt: string;',
  '  readonly sourceVersion: string;',
  '  readonly stats: PriorKnowledgeGraphStats;',
  '  readonly nodes: readonly PriorKnowledgeNode[];',
  '  readonly edges: readonly PriorKnowledgeEdge[];',
  '  readonly seeAlso: string;',
  '}',
  '',
];

const priorKnowledgeIndexModuleLines: readonly string[] = [
  '/**',
  ' * Prior knowledge graph — typed loader for generated JSON data.',
  ' *',
  ' * @remarks',
  ' * Generated by the prior knowledge graph JSON writer.',
  ' * Imports raw JSON data and re-exports it with TypeScript interfaces.',
  ' */',
  "import { createRequire } from 'node:module';",
  'import type {',
  '  PriorKnowledgeEdge,',
  '  PriorKnowledgeGraph,',
  '  PriorKnowledgeGraphStats,',
  '  PriorKnowledgeNode,',
  "} from './types.js';",
  '',
  'const require = createRequire(import.meta.url);',
  "const data: JsonPriorKnowledgeGraph = require('./data.json');",
  '',
  'interface JsonPriorKnowledgeEdge {',
  '  readonly from: string;',
  '  readonly to: string;',
  '  readonly rel: string;',
  '  readonly source: string;',
  '}',
  '',
  'interface JsonPriorKnowledgeNode {',
  '  readonly unitSlug: string;',
  '  readonly unitTitle: string;',
  '  readonly subject: string;',
  '  readonly keyStage: string;',
  '  readonly year?: number;',
  '  readonly priorKnowledge: readonly string[];',
  '  readonly threadSlugs: readonly string[];',
  '}',
  '',
  'interface JsonPriorKnowledgeGraph {',
  '  readonly version: string;',
  '  readonly generatedAt: string;',
  '  readonly sourceVersion: string;',
  '  readonly stats: PriorKnowledgeGraphStats;',
  '  readonly nodes: readonly JsonPriorKnowledgeNode[];',
  '  readonly edges: readonly JsonPriorKnowledgeEdge[];',
  '  readonly seeAlso: string;',
  '}',
  '',
  'function createPriorKnowledgeEdge(edge: JsonPriorKnowledgeEdge): PriorKnowledgeEdge {',
  "  if (edge.rel !== 'prerequisiteFor') {",
  '    throw new TypeError(`Unexpected prior knowledge edge relation: ${edge.rel}`);',
  '  }',
  '',
  "  if (edge.source !== 'thread' && edge.source !== 'priorKnowledge') {",
  '    throw new TypeError(`Unexpected prior knowledge edge source: ${edge.source}`);',
  '  }',
  '',
  '  return {',
  '    from: edge.from,',
  '    to: edge.to,',
  '    rel: edge.rel,',
  '    source: edge.source,',
  '  };',
  '}',
  '',
  'function createPriorKnowledgeNode(node: JsonPriorKnowledgeNode): PriorKnowledgeNode {',
  '  return {',
  '    unitSlug: node.unitSlug,',
  '    unitTitle: node.unitTitle,',
  '    subject: node.subject,',
  '    keyStage: node.keyStage,',
  '    year: node.year,',
  '    priorKnowledge: node.priorKnowledge,',
  '    threadSlugs: node.threadSlugs,',
  '  };',
  '}',
  '',
  'function createPriorKnowledgeGraph(graph: JsonPriorKnowledgeGraph): PriorKnowledgeGraph {',
  '  return {',
  '    version: graph.version,',
  '    generatedAt: graph.generatedAt,',
  '    sourceVersion: graph.sourceVersion,',
  '    stats: graph.stats,',
  '    nodes: graph.nodes.map(createPriorKnowledgeNode),',
  '    edges: graph.edges.map(createPriorKnowledgeEdge),',
  '    seeAlso: graph.seeAlso,',
  '  };',
  '}',
  '',
  '/**',
  ' * Prior knowledge graph with unit dependencies.',
  ' *',
  ' * @remarks',
  ' * Contains unit prior knowledge requirements and learning dependencies.',
  ' * Use for "what comes before" queries and learning path planning.',
  ' */',
  'export const priorKnowledgeGraph: PriorKnowledgeGraph = createPriorKnowledgeGraph(data);',
  '',
  'export type {',
  '  PriorKnowledgeEdge,',
  '  PriorKnowledgeGraph,',
  '  PriorKnowledgeGraphStats,',
  '  PriorKnowledgeNode,',
  "} from './types.js';",
  '',
];

/**
 * Prior knowledge graph dataset descriptor.
 *
 * @remarks
 * Provides the prior-knowledge-graph-specific types and loader content
 * for the generic JSON dataset writer.
 */
export const priorKnowledgeGraphDescriptor: JsonDatasetDescriptor = {
  directoryName: 'prior-knowledge-graph',
  typesModuleContent: priorKnowledgeTypesModuleLines.join('\n'),
  indexModuleContent: priorKnowledgeIndexModuleLines.join('\n'),
};

/**
 * Writes prior knowledge graph output as JSON plus typed TypeScript modules.
 *
 * @param graph - Graph data to write
 * @param outputDir - Parent output directory
 * @returns Path to the created `prior-knowledge-graph` directory
 */
export async function writePriorKnowledgeGraphAsJson(
  graph: PriorKnowledgeGraph,
  outputDir: string,
): Promise<string> {
  return writeJsonDataset(priorKnowledgeGraphDescriptor, graph, outputDir);
}
