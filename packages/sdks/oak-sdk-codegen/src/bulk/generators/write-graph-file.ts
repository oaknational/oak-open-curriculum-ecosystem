/**
 * Generates the file header for prior knowledge graph.
 */
function generatePriorKnowledgeHeader(graph: PriorKnowledgeGraph): readonly string[] {
  return [
    '/**',
    ' * Prior Knowledge Graph — extracted from bulk download data.',
    ' *',
    ' * @remarks',
    ' * - Generated by `pnpm vocab-gen`',
    ` * - Source: ${graph.sourceVersion}`,
    ` * - Generated: ${graph.generatedAt}`,
    ' * - Contains unit prerequisites and learning dependencies',
    ' * - Use for "what comes before" queries and learning path planning',
    ' *',
    ' * @see ADR-086 for extraction methodology',
    ' * @see property-graph-data.ts for the graph pattern',
    ' */',
    '',
  ];
}

/**
 * Generates the export TSDoc for prior knowledge graph.
 */
function generatePriorKnowledgeExportDoc(graph: PriorKnowledgeGraph): readonly string[] {
  const { unitsWithPrerequisites, totalEdges } = graph.stats;
  const subjectCount = graph.stats.subjectsCovered.length;

  return [
    '/**',
    ' * Prior knowledge graph with unit dependencies.',
    ' *',
    ' * @remarks',
    ` * Contains ${unitsWithPrerequisites} units with prerequisites and ${totalEdges} edges across ${subjectCount} subjects.`,
    ' * Edges represent prerequisiteFor relationships derived from thread ordering.',
    ' *',
    ' * Use this to answer questions like:',
    ' * - "What should students know before this unit?"',
    ' * - "What\'s the learning path to trigonometry?"',
    ' * - "Which units depend on this one?"',
    ' */',
  ];
}

/**
 * Serializes a single prior knowledge node.
 */
function serializePriorKnowledgeNode(
  node: PriorKnowledgeGraph['nodes'][number],
): readonly string[] {
  const yearValue = node.year === undefined ? 'undefined' : node.year;

  return [
    '    {',
    `      unitSlug: '${node.unitSlug}',`,
    `      unitTitle: '${escapeString(node.unitTitle)}',`,
    `      subject: '${node.subject}',`,
    `      keyStage: '${node.keyStage}',`,
    `      year: ${yearValue},`,
    `      priorKnowledge: ${JSON.stringify(node.priorKnowledge)},`,
    `      threadSlugs: ${JSON.stringify(node.threadSlugs)},`,
    '    },',
  ];
}

/**
 * Serializes a single prior knowledge edge.
 */
function serializePriorKnowledgeEdge(
  edge: PriorKnowledgeGraph['edges'][number],
): readonly string[] {
  return [
    `    { from: '${edge.from}', to: '${edge.to}', rel: '${edge.rel}', source: '${edge.source}' },`,
  ];
}

/**
 * Generates the prior knowledge graph body with metadata, nodes, and edges.
 *
 * @remarks
 * Uses explicit type annotation `: PriorKnowledgeGraph` instead of `as const`
 * because the graph is too large for TypeScript to serialize the literal type.
 */
function generatePriorKnowledgeGraphBody(graph: PriorKnowledgeGraph): readonly string[] {
  return [
    'export const priorKnowledgeGraph: PriorKnowledgeGraph = {',
    `  version: '${graph.version}',`,
    `  generatedAt: '${graph.generatedAt}',`,
    `  sourceVersion: '${graph.sourceVersion}',`,
    '',
    '  stats: {',
    `    unitsWithPrerequisites: ${graph.stats.unitsWithPrerequisites},`,
    `    totalEdges: ${graph.stats.totalEdges},`,
    `    subjectsCovered: ${JSON.stringify(graph.stats.subjectsCovered)},`,
    '  },',
    '',
    '  nodes: [',
    ...graph.nodes.flatMap(serializePriorKnowledgeNode),
    '  ],',
    '',
    '  edges: [',
    ...graph.edges.flatMap(serializePriorKnowledgeEdge),
    '  ],',
    '',
    `  seeAlso: '${escapeString(graph.seeAlso)}',`,
    '};',
    '',
  ];
}

/**
 * Generates the interface type definitions for prior knowledge graph.
 *
 * @remarks
 * We use explicit interface types rather than `typeof` because the graph
 * data is too large for TypeScript to serialize the literal type.
 */
function generatePriorKnowledgeTypeExports(): readonly string[] {
  return [
    '/**',
    ' * A prior knowledge edge in the graph.',
    ' */',
    'export interface PriorKnowledgeEdge {',
    '  readonly from: string;',
    '  readonly to: string;',
    "  readonly rel: 'prerequisiteFor';",
    "  readonly source: 'thread' | 'priorKnowledge';",
    '}',
    '',
    '/**',
    ' * A unit node in the prior knowledge graph.',
    ' */',
    'export interface PriorKnowledgeNode {',
    '  readonly unitSlug: string;',
    '  readonly unitTitle: string;',
    '  readonly subject: string;',
    '  readonly keyStage: string;',
    '  readonly year: number | undefined;',
    '  readonly priorKnowledge: readonly string[];',
    '  readonly threadSlugs: readonly string[];',
    '}',
    '',
    '/**',
    ' * Statistics about the prior knowledge graph.',
    ' */',
    'export interface PriorKnowledgeGraphStats {',
    '  readonly unitsWithPrerequisites: number;',
    '  readonly totalEdges: number;',
    '  readonly subjectsCovered: readonly string[];',
    '}',
    '',
    '/**',
    ' * The complete prior knowledge graph structure.',
    ' */',
    'export interface PriorKnowledgeGraph {',
    '  readonly version: string;',
    '  readonly generatedAt: string;',
    '  readonly sourceVersion: string;',
    '  readonly stats: PriorKnowledgeGraphStats;',
    '  readonly nodes: readonly PriorKnowledgeNode[];',
    '  readonly edges: readonly PriorKnowledgeEdge[];',
    '  readonly seeAlso: string;',
    '}',
    '',
  ];
}

/**
 * Serializes a PriorKnowledgeGraph to TypeScript source code.
 *
 * @param graph - The graph data to serialize
 * @returns TypeScript source code string
 *
 * @remarks
 * The order is: header → types → doc → data.
 * Types must come before the data export because the data uses the type annotation.
 */
export function serializePriorKnowledgeGraph(graph: PriorKnowledgeGraph): string {
  const lines: string[] = [
    ...generatePriorKnowledgeHeader(graph),
    ...generatePriorKnowledgeTypeExports(),
    ...generatePriorKnowledgeExportDoc(graph),
    ...generatePriorKnowledgeGraphBody(graph),
  ];

  return lines.join('\n');
}

/**
 * Writes a prior knowledge graph to a TypeScript file.
 *
 * @param graph - The graph data to write
 * @param outputDir - Directory to write the file to
 * @returns Path to the written file
 */
export async function writePriorKnowledgeGraphFile(
  graph: PriorKnowledgeGraph,
  outputDir: string,
  logger?: Logger,
): Promise<string> {
  logger?.info('bulk.writer.write_prior_knowledge_graph', {
    outputDir,
    sourceVersion: graph.sourceVersion,
  });
  const fileName = 'prior-knowledge-graph-data.ts';
  const filePath = join(outputDir, fileName);
  const content = serializePriorKnowledgeGraph(graph);

  await writeFile(filePath, content, 'utf-8');

  return filePath;
}
